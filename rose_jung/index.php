                 <h2 id="bout">About The Artist</h2>
                 <p class="bio"> 
                 &nbsp; Rose Jung is an Southwest Florida mixed media artist.  Her artwork incorporates recycled and re-purposed materials. The re-purposed piece  “Aquamarina: Goddess of clean water” utilizes recycled natural and man made materials as an express of concern for the toxic waterways of Florida.  She  uses masks as symbols of concealment of the ego   or  an expression of different entities in  her artwork.  Butterflies are also a common characteristic in her artistic creations as symbols of transformation, hope and life as transitory.   <br/><br/>
                 &nbsp; She seeks inspiration from the current social, political and cultural climates.  Years of extensive traveling have exposed to her to Asian and Pacific Rim art forms;  when possible she imbues elements of it in her sculptural pieces. Her art has been seen in juried shows at the Sidney and Berne Davis Art Center, The Alliance for the Arts, Arts for Act,  Visual Arts Center Punta Gorda, Coco Art Gallery, Estero and Centers for the Arts Bonita Springs.
                 </p>
